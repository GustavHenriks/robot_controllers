    def pubish_on_point_cloud(self):
        pointCloud = PointCloud()
        header = Header()
        header.stamp = rospy.Time.now()
        header.frame_id = 'world'
        pointCloud.header = header
        pointCloud.points = []
        for i in range(len(self.x_vec)):
            Point_temp = Point32()
            Point_temp.x = self.x_vec[i]
            Point_temp.y = self.y_vec[i]
            Point_temp.z = self.z_vec[i]

            pointCloud.points.append(Point_temp)
        self.CloudPub.publish(pointCloud)

